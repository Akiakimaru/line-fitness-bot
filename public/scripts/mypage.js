    const logsUrl = `/user/logs?uid=${encodeURIComponent(uid)}&exp=${encodeURIComponent(exp)}&sig=${encodeURIComponent(sig)}&days=8`;
    
    console.log('API URLs:', { summaryUrl, logsUrl });
    
